package com.comcast.vde.cm.model;

public class Ads {

    private String adBreakWindow;
    private String channelName;
    private String acrUrl;
    private String psnUrl;
    private Boolean enable;
    private String adZone;
    private String adType;
    private Integer udpPort;

    public String getAdBreakWindow() {
        return adBreakWindow;
    }

    public void setAdBreakWindow(String adBreakWindow) {
        this.adBreakWindow = adBreakWindow;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public String getAcrUrl() {
        return acrUrl;
    }

    public void setAcrUrl(String acrUrl) {
        this.acrUrl = acrUrl;
    }

    public String getPsnUrl() {
        return psnUrl;
    }

    public void setPsnUrl(String psnUrl) {
        this.psnUrl = psnUrl;
    }

    public Boolean getEnable() {
        return enable;
    }

    public void setEnable(Boolean enable) {
        this.enable = enable;
    }

    public String getAdZone() {
        return adZone;
    }

    public void setAdZone(String adZone) {
        this.adZone = adZone;
    }

    public String getAdType() {
        return adType;
    }

    public void setAdType(String adType) {
        this.adType = adType;
    }

    public Integer getUdpPort() {
        return udpPort;
    }

    public void setUdpPort(Integer udpPort) {
        this.udpPort = udpPort;
    }

  

}
